#!/bin/bash
# ================================================================
#  ログをリアルタイムで確認する（バックグラウンド実行中に使用）
# ================================================================

DIR="$(cd "$(dirname "$0")" && pwd)"
LOG="$DIR/scanner.log"

if [ ! -f "$LOG" ]; then
    echo "[INFO] scanner.log がまだ存在しません。Botを起動してください。"
    exit 0
fi

echo "=============================="
echo "  scanner.log を監視中..."
echo "  Ctrl+C で終了"
echo "=============================="
echo ""
tail -f "$LOG"
